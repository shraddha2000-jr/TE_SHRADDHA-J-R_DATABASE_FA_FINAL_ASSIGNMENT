--------------JOIN------------

SELECT H.HOS_ID,H.HOS_NAME,D.D_ID,D.D_NAME,D.D_SPECIFICATION,P.P_ID,P.P_NAME
FROM TE_FA_HOSPITAL H
INNER JOIN TE_FA__DOCTOR D
ON H.HOS_ID=D.HOS_ID
INNER JOIN TE_FA__PATIENT P
ON D.D_ID=P.D_ID


